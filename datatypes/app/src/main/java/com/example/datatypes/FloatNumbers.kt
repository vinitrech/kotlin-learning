    //declaring Float variable
    val myNumber = 2.5f // 32 bits width
    val maxFloatValue = Float.MAX_VALUE
    val minFloatValue = Float.MIN_VALUE

    println("Float maximum value: $maxFloatValue")
    println("Float minimum value: $minFloatValue\n")

    val myDouble = 2.5 // 64 bits width
